export function hasAiDeveloperKeyword(parts: Array<string | string[] | null | undefined>): boolean {
  const searchText = parts
    .flatMap((part) => Array.isArray(part) ? part : [part])
    .filter((part): part is string => Boolean(part))
    .join(' ')
    .toLowerCase();

  return AI_DEV_RELEVANT_KEYWORDS.some((keyword) => searchText.includes(keyword));
}
